Gold for centuries has been used as a standard for monetary exchange, in bullion, coinage and jewellery.

The gold content of gold alloys is measured in karats (abbreviation K). A karat, when used with gold, is a unit of
purity – 24 karat gold is pure gold, but usually you mix gold with a metal like copper or silver to make jewelry
(because pure gold is too soft). Each karat indicates 1/24th of the whole. So if a piece of jewelry is made of metal
