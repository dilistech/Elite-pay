    <p>How do cryptocurrency markets work?
        Cryptocurrency markets are decentralised, which means they are not issued or backed by a central authority such
        as a government. Instead, they run across a network of computers. However, cryptocurrencies can be bought and
        sold via exchanges and stored in ‘wallets’ .

        Unlike traditional currencies, cryptocurrencies exist only as a shared digital record of ownership, stored on a
        blockchain. When a user wants to send cryptocurrency units to another user, they send it to that user’s digital
        wallet. The transaction isn’t considered final until it has been verified and added to the blockchain through a
        process called mining. This is also how new cryptocurrency tokens are usually created.

        What is blockchain?
        A blockchain is a shared digital register of recorded data. For cryptocurrencies, this is the transaction
        history for every unit of the cryptocurrency, which shows how ownership has changed over time. Blockchain works
        by recording transactions in ‘blocks’, with new blocks added at the front of the chain.</p>
    <p>Blockchain technology has unique security features that normal computer files do not have.

        Network consensus

        A blockchain file is always stored on multiple computers across a network – rather than in a single location –
        and is usually readable by everyone within the network. This makes it both transparent and very difficult to
        alter, with no one weak point vulnerable to hacks, or human or software error.

        Cryptography

        Blocks are linked together by cryptography – complex mathematics and computer science. Any attempt to alter data
        disrupts the cryptographic links between blocks, and can quickly be identified as fraudulent by computers in the
        network.

        What is cryptocurrency mining?
        Cryptocurrency mining is the process by which recent cryptocurrency transactions are checked and new blocks are
        added to the blockchain.

        Checking transactions

        Mining computers select pending transactions from a pool and check to ensure that the sender has sufficient
        funds to complete the transaction. This involves checking the transaction details against the transaction
        history stored in the blockchain. A second check confirms that the sender authorised the transfer of funds using
        their private key.</p>
    <p>Creating a new block

        Mining computers compile valid transactions into a new block and attempt to generate the cryptographic link to
        the previous block by finding a solution to a complex algorithm. When a computer succeeds in generating the
        link, it adds the block to its version of the blockchain file and broadcasts the update across the network.</p>
    <p>How does cryptocurrency trading work?
        With IG, you can trade cryptocurrencies via a CFD account – derivative products that enable you speculate on
        whether your chosen cryptocurrency will rise or fall in value. Prices are quoted in traditional currencies such
        as the US dollar, and you never take ownership of the cryptocurrency itself.

        CFDs are leveraged products, which means you can open a position for a just a fraction of the full value of the
        trade. Although leveraged products can magnify your profits, they can also magnify losses if the market moves
        against you.

        What is the spread in cryptocurrency trading?
        The spread is the difference between the buy and sell prices quoted for a cryptocurrency. Like many financial
        markets, when you open a position on a cryptocurrency market, you’ll be presented with two prices. If you want
        to open a long position, you trade at the buy price, which is slightly above the market price. If you want to
        open a short position, you trade at the sell price – slightly below the market price.

        What is a lot in cryptocurrency trading?
        Cryptocurrencies are often traded in lots – batches of cryptocurrency tokens used to standardise the size of
        trades. As cryptocurrencies are very volatile, lots tend to be very small: most are just one unit of the base
        cryptocurrency. However, some cryptocurrencies are traded in bigger lots.

        What is leverage in cryptocurrency trading?
        Leverage is the means of gaining exposure to large amounts of cryptocurrency without having to pay the full
        value of your trade upfront. Instead, you put down a small deposit, known as margin. When you close a leveraged
        position, your profit or loss is based on the full size of the trade.</p>
